    /**
     * Function to get latitude
     */
    public double getLatitude() {
        if (location != null) {
            latitude = location.getLatitude();
        }

        return latitude;
    }

    /**
     * Function to get longitude
     */
    public double getLongitude() {
        if (location != null) {
            longitude = location.getLongitude();
        }

        return longitude;
    }

    /**
     * Function to check GPS/wifi enabled
     */
    public boolean canGetLocation() {
        return this.canGetLocation;
    }

    /**
     * Function to show settings alert dialog
     */

    public void showSettingsAlert() {

        String title = "Location";
        String message = "Enable your GPS";
        String btn1Text = "Enable";
        String btn2Text = "Cancel";

        DialogInterface.OnClickListener dialogClickListener = new DialogInterface.OnClickListener() {
            @Override
            public void onClick(DialogInterface dialog, int which) {
                switch (which){
                    case DialogInterface.BUTTON_POSITIVE:
                        Intent intent = new Intent(
                                Settings.ACTION_LOCATION_SOURCE_SETTINGS);
                        mContext.startActivity(intent);
                        break;

                    case DialogInterface.BUTTON_NEGATIVE:
                        if (!isGPSEnabled()) {
                            showSettingsAlert();
                        }
                        break;
                }
            }
        };

        AlertDialog.Builder builder = new AlertDialog.Builder(mContext);
        AlertDialog dialog = builder.setTitle(title).setMessage(message).setPositiveButton(btn1Text, dialogClickListener)
                .setNegativeButton(btn2Text, dialogClickListener).show();
        dialog.setCanceledOnTouchOutside(false);
    }

    @Override
    public void onLocationChanged(Location location) {

        if (mContext != null && location != null) {
            // updating location on Shared Preference
            AppPreferences.getInstance().setLocation(
                    location.getLatitude(), location.getLongitude());
//            mContext.sendBroadcast(new Intent(mContext,
//                    LocationUpdatedBroadcastReceiver.class));
        }
    }


